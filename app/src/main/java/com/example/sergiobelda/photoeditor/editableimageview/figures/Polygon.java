package com.example.sergiobelda.photoeditor.editableimageview.figures;

public abstract class Polygon {
    private float x, y;

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
}
